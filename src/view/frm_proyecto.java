    private void txt_cedularesponsableKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_cedularesponsableKeyReleased
            class_proyecto modelo_proyecto = null;
            
                
        if ( ( (evt.getKeyCode() >= 48 ) && (evt.getKeyCode() <= 57 ) ) || ( (evt.getKeyCode() >= 37 ) && (evt.getKeyCode() <= 40 ) ) || ((evt.getKeyCode() >= 96 ) && (evt.getKeyCode() <= 105 )) || (evt.getKeyCode() == 144) || (evt.getKeyCode() == 127) || (evt.getKeyCode() == 8)  || (evt.getKeyCode() == 10)){       
            try {
                ctrl_proyecto proyecto = new ctrl_proyecto(0,"","","","",Integer.parseInt(txt_cedularesponsable.getText().trim()),"","","",0);
                
                if(!proyecto.buscarnombreresponsable().equals("")){
                    txt_cedularesponsable.setText(txt_cedularesponsable.getText().trim());
                    txt_nombreresponsable.setText(proyecto.buscarnombreresponsable());
                }
                else{
                    txt_nombreresponsable.setText("");
                }
           
            }            
            catch (NumberFormatException ex) {
                txt_nombreresponsable.setText("");
            }
        }
        else{
             if (evt.getKeyCode() == 27){
         
                    bot_reiniciar.doClick();
                    
             }
             else{
                JOptionPane.showMessageDialog(null, "CARÁCTER NO VÁLIDO. SOLO NÚMEROS (1..9)", "SISTIMACA - USUARIO", JOptionPane.ERROR_MESSAGE,null);
                txt_cedularesponsable.setText("");
             }   
        }
            
            
            

    }//GEN-LAST:event_txt_cedularesponsableKeyReleased

