    private void txt_idproyectoactKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_idproyectoactKeyReleased
        class_proyecto modelo_proyecto;
        ctrl_proyecto proyecto = new ctrl_proyecto(0,txt_nuevoidproyecto.getText().trim(),"","","",0,"","","",0);
        
        if (txt_idproyecto.getText().trim().length() > 3){
            modelo_proyecto = proyecto.consulta_fechasproyecto();

                    if (modelo_proyecto.isStatus()){


                        txt_nuevaproyecto.setText(modelo_proyecto.getDescripcionproyecto());
                        txt_nuevoidproyecto.setText(modelo_proyecto.getId_proyecto());
                        txt_cedularesponsablenuevo.setText("");
                        txt_nombreresponsablenuevo.setText("");
                        cmb_tipoordennuevo.setSelectedItem(modelo_proyecto.getTipo_orden());
                        txt_nuevoorden.setText(modelo_proyecto.getNro_orden());
                        txt_nuevocliente.setText(modelo_proyecto.getCliente());
                        txt_cedularesponsablenuevo.setText(modelo_proyecto.getCedula().toString());
                        txt_nombreresponsablenuevo.setText(modelo_proyecto.getNombres());
                        date_nuevodesde.setDate(Date.valueOf(funciones.fecha_bd_usuario(modelo_proyecto.getFechadesde().trim(),2)));
                        date_nuevohasta.setDate(Date.valueOf(funciones.fecha_bd_usuario(modelo_proyecto.getFechahasta().trim(),2)));
                        
                        txt_nuevoidproyecto.setEnabled(true);
                        txt_nuevaproyecto.setEnabled(true);
                        date_nuevodesde.setEnabled(true);
                        date_nuevohasta.setEnabled(true);
                        cmb_tipoordennuevo.setEnabled(true);
                        txt_nuevoorden.setEnabled(true);
                        txt_nuevocliente.setEnabled(true);
                        txt_cedularesponsablenuevo.setEnabled(true); 
                        txt_nombreresponsablenuevo.setEnabled(true);       
                        
                        
                        bot_buscarrespact.setEnabled(true);
                       

                    }
        }
    }//GEN-LAST:event_txt_idproyectoactKeyReleased

