    private void bot_crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_crearActionPerformed
      class_empresa modelo_empresa;
      ctrl_comun control;
        
        if(!txt_idempresa.getText().equals("") && !txt_empresa.getText().equals("") ) {
                
                ctrl_empresa empresa = new ctrl_empresa(Integer.parseInt(txt_idempresa.getText()),txt_empresa.getText(),0);
                               
                modelo_empresa = empresa.actualizar_empresa();
                
                if (modelo_empresa.isStatus()){
                    
                   empresa.consulta_empresa(table_empresa);
                         
                   control = new ctrl_comun(4); 
                   cmb_empresa =  control.cargar_combo(cmb_empresa,txt_empresa.getText().toUpperCase().trim());
                   
                }
                
        }
        else{
            JOptionPane.showMessageDialog(null,"DATOS INCOMPLETOS PARA GUARDAR");
        } 
    }//GEN-LAST:event_bot_crearActionPerformed

