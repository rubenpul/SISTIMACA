    private void bot_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_actualizarActionPerformed
        class_empresa modelo_empresa= null; 
        ctrl_comun control;
        boolean resp = true;
       
        
        if(!txt_nuevaempresa.getText().equals("") && !txt_nuevoidempresa.getText().equals("") && !cmb_empresa.getSelectedItem().equals("Seleccione...")  ) {

           
            
            if (!txt_nuevoidempresa.getText().equals(cmb_empresa.getSelectedItem().toString().substring(0,3).trim())){
                
                ctrl_empresa empresa = new ctrl_empresa(Integer.parseInt(txt_nuevoidempresa.getText()),"",0);
                
                modelo_empresa = empresa.buscaridempresa();
                
                resp =  modelo_empresa.isStatus();
            }
            
            if (resp){
                
                ctrl_empresa empresa = new ctrl_empresa(Integer.parseInt(txt_nuevoidempresa.getText()),txt_nuevaempresa.getText(),Integer.parseInt(cmb_empresa.getSelectedItem().toString().substring(0,3).trim()));
                
                modelo_empresa = empresa.actualizar_empresa_detalle();

                if (modelo_empresa.isStatus()){
                    
                    empresa.consulta_empresa(table_empresa);
                                                    
                    control = new ctrl_comun(4); 
                    cmb_empresa =  control.cargar_combo(cmb_empresa,txt_empresa.getText().toUpperCase().trim());
                    
                    txt_nuevaempresa.setText("");
                    
                    txt_nuevoidempresa.setText("");
                                   
                }
            }
        }
        else{

            JOptionPane.showMessageDialog(null,"DATOS INCOMPLETOS PARA ACTUALIZAR");
        }
    }//GEN-LAST:event_bot_actualizarActionPerformed

