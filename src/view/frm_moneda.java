    private void bot_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_actualizarActionPerformed
        class_moneda modelo_moneda= null; 
        ctrl_comun control;
        boolean resp = true;
       
        
        if(!txt_nuevamoneda.getText().equals("") && !txt_nuevacodmoneda.getText().equals("") && !cmb_moneda.getSelectedItem().equals("Seleccione...")  ) {

           
            
            if (!txt_nuevacodmoneda.getText().equals(cmb_moneda.getSelectedItem().toString().substring(5,cmb_moneda.getSelectedItem().toString().length()).trim())){
                
                ctrl_moneda moneda = new ctrl_moneda(Integer.parseInt(cmb_moneda.getSelectedItem().toString().substring(0,3).trim()),txt_nuevacodmoneda.getText(),txt_nuevamoneda.getText().trim().toUpperCase());
                
                modelo_moneda = moneda.actualizar_moneda_detalle();
                
                resp =  modelo_moneda.isStatus();
                
                if (resp){ 
                    moneda.consulta_moneda(table_moneda);
                    
                    control = new ctrl_comun(6); 
                    cmb_moneda =  control.cargar_combo(cmb_moneda,txt_nuevamoneda.getText().toUpperCase().trim());
                    
                    txt_nuevamoneda.setText("");
                    
                }
                
            }
            
        }
        else{

            JOptionPane.showMessageDialog(null,"DATOS INCOMPLETOS PARA ACTUALIZAR");
        }
    }//GEN-LAST:event_bot_actualizarActionPerformed

