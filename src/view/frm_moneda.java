    private void bot_crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_crearActionPerformed
      class_moneda modelo_moneda;
      ctrl_comun control;
        
        if(!txt_moneda.getText().equals("") && !txt_codmoneda.getText().equals("") ) {
                
                ctrl_moneda moneda = new ctrl_moneda(0,txt_codmoneda.getText().toUpperCase().trim(),txt_moneda.getText().toUpperCase().trim());
                               
                modelo_moneda = moneda.actualizar_moneda();
                
                if (modelo_moneda.isStatus()){
                    
                   moneda.consulta_moneda(table_moneda);
                         
                   control = new ctrl_comun(6); 
                   cmb_moneda =  control.cargar_combo(cmb_moneda,txt_codmoneda.getText().toUpperCase().trim());
                   
                }
                
        }
        else{
            JOptionPane.showMessageDialog(null,"DATOS INCOMPLETOS PARA GUARDAR");
        } 
    }//GEN-LAST:event_bot_crearActionPerformed

