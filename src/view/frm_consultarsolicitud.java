    public static void consultar_solicitud_proyecto(){
    
    
        Integer opcion = 5;
        String fechadesde = funciones.fecha_convertir_date(date_desde, 1);
        String fechahasta = funciones.fecha_convertir_date(date_hasta, 1);

        Integer tiposolicitud;
        Integer nrosolicitud;

       
        if(!cmb_tiposolicitud.getSelectedItem().equals("Seleccione...")){
            tiposolicitud = Integer.parseInt(cmb_tiposolicitud.getSelectedItem().toString().substring(0,3).trim());
        }
        else{
            tiposolicitud = 0;
        }

        if (!txt_nrosolicitud.getText().trim().equals("")){
            nrosolicitud = Integer.parseInt(txt_nrosolicitud.getText().trim());
        }
        else{
            nrosolicitud = 0;
        }

        ctrl_solicitud solicitud =  new ctrl_solicitud(

            nrosolicitud,
            Integer.parseInt(frm_principalM1.lbl_cedula.getText().trim()),
            Integer.parseInt(frm_principalM1.lbl_area.getText().trim()),
            frm_principalM1.lbl_desarea.getText().trim(),
            tiposolicitud,
            "",
            0,
            0, //numero de material
            "",             // descripcion de material
            0,
            "",
            Integer.parseInt(txt_id.getText().trim()),
            "",
            0,
            0,
            0,
            0,
            0,
            "", //fecha solicitud
            "",
            "",
            false,
            "",
            "",
            "",
            "",
            "",
            "",
            "",
            "",
            ""    
        );

        if(radio_pendiente.isSelected()){
            opcion = 0;
        }

        if(radio_conformada.isSelected()){
            opcion = 1;
        }

        if(radio_aprobada.isSelected()){
            opcion = 2;
        }

        if (radio_autorizada.isSelected()){
            opcion = 3;
        }

        if(radio_anulada.isSelected()){
            opcion = 4;
        }

        solicitud.consultar_solicitudes_fase(tabla_resumensolicitud,opcion,fechadesde,fechahasta,Integer.parseInt(frm_principalM1.lbl_nivel.getText().trim()));

        // solicitud.consultar_solicitudes_activa(tabla_resumensolicitud,3,"","",opcion,Integer.parseInt(frm_principalM1.lbl_nivel.getText().trim()));
 
    
}
    
