    private void bot_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_actualizarActionPerformed
        class_umb modelo_umb= null; 
        ctrl_comun control;
        boolean resp = true;
       
        
        if(!txt_nuevaumb.getText().equals("") && !txt_nuevacodumb.getText().equals("") && !cmb_umb.getSelectedItem().equals("Seleccione...")  ) {

           
            
            if (!txt_nuevacodumb.getText().equals(cmb_umb.getSelectedItem().toString().substring(5,cmb_umb.getSelectedItem().toString().length()).trim())){
                
                ctrl_umb umb = new ctrl_umb(Integer.parseInt(cmb_umb.getSelectedItem().toString().substring(0,3).trim()),txt_nuevacodumb.getText().toUpperCase().trim(),txt_nuevaumb.getText().trim().toUpperCase());
                
                modelo_umb = umb.actualizar_umb_detalle();
                
                resp =  modelo_umb.isStatus();
                
                if (resp){ 
                    umb.consulta_umb(table_umb);
                    
                    control = new ctrl_comun(13); 
                    cmb_umb =  control.cargar_combo(cmb_umb,txt_nuevaumb.getText().toUpperCase().trim());
                    
                    txt_nuevacodumb.setText("");
                    txt_nuevaumb.setText("");
                    
                }
                
            }
            
        }
        else{

            JOptionPane.showMessageDialog(null,"DATOS INCOMPLETOS PARA ACTUALIZAR");
        }
    }//GEN-LAST:event_bot_actualizarActionPerformed

