    private void bot_crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_crearActionPerformed
      class_umb modelo_umb;
      ctrl_comun control;
        
        if(!txt_umb.getText().equals("") && !txt_codumb.getText().equals("") ) {
                
                ctrl_umb umb = new ctrl_umb(0,txt_codumb.getText().toUpperCase().trim(),txt_umb.getText().toUpperCase().trim());
                               
                modelo_umb = umb.actualizar_umb();
                
                if (modelo_umb.isStatus()){
                    
                   umb.consulta_umb(table_umb);
                         
                   control = new ctrl_comun(13); 
                   cmb_umb =  control.cargar_combo(cmb_umb,txt_codumb.getText().toUpperCase().trim());
                   
                }
                
        }
        else{
            JOptionPane.showMessageDialog(null,"DATOS INCOMPLETOS PARA GUARDAR");
        } 
    }//GEN-LAST:event_bot_crearActionPerformed

