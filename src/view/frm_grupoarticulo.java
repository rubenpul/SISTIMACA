    private void bot_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_actualizarActionPerformed
        class_grupomaterial modelo_grupomaterial= null; 
        ctrl_comun control;
        boolean resp = true;
       
        
        if(!txt_nuevagrupomaterial.getText().equals("") && !txt_nuevoidgrupomaterial.getText().equals("") && !cmb_grupomaterial.getSelectedItem().equals("Seleccione...")  ) {

           
            
            if (!txt_nuevoidgrupomaterial.getText().equals(cmb_grupomaterial.getSelectedItem().toString().substring(0,3).trim())){
                
                ctrl_grupomaterial grupomaterial = new ctrl_grupomaterial(Integer.parseInt(txt_nuevoidgrupomaterial.getText()),"",0);
                
                modelo_grupomaterial = grupomaterial.buscaridgrupomaterial();
                
                resp =  modelo_grupomaterial.isStatus();
            }
            
            if (resp){
                
                ctrl_grupomaterial grupomaterial = new ctrl_grupomaterial(Integer.parseInt(txt_nuevoidgrupomaterial.getText()),txt_nuevagrupomaterial.getText(),Integer.parseInt(cmb_grupomaterial.getSelectedItem().toString().substring(0,3).trim()));
                
                modelo_grupomaterial = grupomaterial.actualizar_grupomaterial_detalle();

                if (modelo_grupomaterial.isStatus()){
                    
                    grupomaterial.consulta_grupomaterial(table_grupomaterial);
                                                    
                    control = new ctrl_comun(5); 
                    cmb_grupomaterial =  control.cargar_combo(cmb_grupomaterial,txt_grupomaterial.getText().toUpperCase().trim());
                    
                    txt_nuevagrupomaterial.setText("");
                    
                    txt_nuevoidgrupomaterial.setText("");
                                   
                }
            }
        }
        else{

            JOptionPane.showMessageDialog(null,"DATOS INCOMPLETOS PARA ACTUALIZAR");
        }
    }//GEN-LAST:event_bot_actualizarActionPerformed

