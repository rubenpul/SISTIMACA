    private void bot_crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_crearActionPerformed
      class_grupomaterial modelo_grupomaterial;
      ctrl_comun control;
        
        if(!txt_idgrupomaterial.getText().equals("") && !txt_grupomaterial.getText().equals("") ) {
                
                ctrl_grupomaterial grupomaterial = new ctrl_grupomaterial(Integer.parseInt(txt_idgrupomaterial.getText()),txt_grupomaterial.getText(),0);
                               
                modelo_grupomaterial = grupomaterial.actualizar_grupomaterial();
                
                if (modelo_grupomaterial.isStatus()){
                    
                   grupomaterial.consulta_grupomaterial(table_grupomaterial);
                         
                   control = new ctrl_comun(5); 
                   cmb_grupomaterial =  control.cargar_combo(cmb_grupomaterial,txt_grupomaterial.getText().toUpperCase().trim());
                   
                }
                
        }
        else{
            JOptionPane.showMessageDialog(null,"DATOS INCOMPLETOS PARA GUARDAR");
        } 
    }//GEN-LAST:event_bot_crearActionPerformed

