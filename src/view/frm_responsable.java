    private void bot_seleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_seleccionarActionPerformed
        Integer fila;
        
        fila = tabla_usuario.getSelectedRow();
        switch (this.opcion){
            case 1:      
                frm_proyecto.txt_cedularesponsable.setText(tabla_usuario.getValueAt(fila, 0).toString());
                frm_proyecto.txt_nombreresponsable.setText(tabla_usuario.getValueAt(fila, 1).toString());
                break;
            case 2:  
                frm_proyecto.txt_cedularesponsablenuevo.setText(tabla_usuario.getValueAt(fila, 0).toString());
                frm_proyecto.txt_nombreresponsablenuevo.setText(tabla_usuario.getValueAt(fila, 1).toString());
                break;
        }
         this.dispose();
    }//GEN-LAST:event_bot_seleccionarActionPerformed

