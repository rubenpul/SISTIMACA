    private void bot_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_actualizarActionPerformed
       Integer cantidad;
       Integer fila;
       
      
       fila = table_detalleentrada.getSelectedRow();
        
       cantidad = Integer.parseInt(table_detalleentrada.getValueAt(fila,11).toString());
        
       if (cantidad >= Integer.parseInt(spin_cantidad.getValue().toString())){
           
                ctrl_entrada entrada  = new ctrl_entrada(
                    0, //id_entrada,
                    0, 
                    Integer.parseInt(table_detalleentrada.getValueAt(fila,1).toString()), //id_material, 
                    table_detalleentrada.getValueAt(fila,2).toString(), //descripcionmaterial
                    Integer.parseInt(table_detalleentrada.getValueAt(fila,13).toString()), //idvaloracion
                    table_detalleentrada.getValueAt(fila,7).toString(), //descripcionvaloracion
                    1,  //moneda
                    "", // descripcion_moneda,  
                    Integer.parseInt(cmb_almacen.getSelectedItem().toString().substring(0,3).trim()), //id_almacen,
                    cmb_almacen.getSelectedItem().toString().substring(4,cmb_almacen.getSelectedItem().toString().length()).trim(), //descripcion_almacen,
                    Integer.parseInt(cmb_ubicacion.getSelectedItem().toString().substring(0,3).trim()),
                    cmb_ubicacion.getSelectedItem().toString().substring(4,cmb_ubicacion.getSelectedItem().toString().length()).trim(), //ubicacion
                    Integer.parseInt(table_detalleentrada.getValueAt(fila,12).toString()),
                    table_detalleentrada.getValueAt(fila,8).toString(), //procedencia
                    "",
                    Integer.parseInt(table_detalleentrada.getValueAt(fila,0).toString()), //iddocumento
                    0,//idcompra
                    0, //nrofactura
                    table_detalleentrada.getValueAt(fila,10).toString(), //fecha_entrada,
                    cantidad, //cantidad_ingresada,
                    0.0, //precio_unitario,
                    0.0, //monto,
                    0.0, //flete
                    0.0, //monto
                    "", //observacion
                    true       
                ); 
                
                   entrada.actualizar_documento_entrada(table_detalleentrada);
                  
       }
       else{
           JOptionPane.showMessageDialog(null,"LA CANTIDAD A ACTUALIZAR SUPERA A LA REGISTRADA EN EL DOCUMENTO");
       }
       
       
    }//GEN-LAST:event_bot_actualizarActionPerformed

