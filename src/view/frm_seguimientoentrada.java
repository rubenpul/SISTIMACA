    private void table_detalleentradaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_detalleentradaMouseClicked
        Integer fila;
        ctrl_solicitud solicitud;
        BufferedImage img;
        
        
        fila = table_detalleentrada.getSelectedRow();
        
        lbl_desdocumento.setText(table_detalleentrada.getValueAt(fila,0).toString());
        lbl_desnumero.setText(table_detalleentrada.getValueAt(fila,1).toString()); 
        spin_cantidad.setValue(table_detalleentrada.getValueAt(fila,11));
        lbl_desdescripcion.setText(table_detalleentrada.getValueAt(fila,2).toString());
        
        ctrl_comun control = new ctrl_comun(1);
        cmb_almacen =  control.cargar_combo(cmb_almacen,table_detalleentrada.getValueAt(fila,3).toString());
        control = new ctrl_comun(14);
        cmb_ubicacion =  control.cargar_combo(cmb_ubicacion,table_detalleentrada.getValueAt(fila,4).toString());
        
        solicitud = new ctrl_solicitud(
                
                        0,
                        0,
                        0,
                        "",
                        0,
                        "",
                        0,
                        Integer.parseInt(lbl_desnumero.getText()), //numero de material
                        "",             // descripcion de material     
                        0,
                        "",
                        0,
                        "",
                        0,    
                        0,
                        "", //fecha solicitud
                        "",
                        "",
                        false,
                        "",
                        "",
                        "",
                        "",
                        "",
                        "",
                        "",
                        ""
                );  
        
        
        
        
        
        try{  
            img = funciones.decodeToImage(solicitud.consultar_foto());
       
            ImageIcon icon = new ImageIcon(img);
            Icon icono = new ImageIcon(icon.getImage().getScaledInstance(frm_solicitud.lbl_foto.getWidth(), frm_solicitud.lbl_foto.getHeight(), Image.SCALE_DEFAULT));
            lbl_foto.setText(null);
            lbl_foto.setIcon(icono);
            
        }  
        catch(NullPointerException ex){  }
        
        
        
    }//GEN-LAST:event_table_detalleentradaMouseClicked

