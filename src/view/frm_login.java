    private void bot_ingresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_ingresarActionPerformed
       String sql;
        
        String password = new String(txt_password.getPassword());
        boolean resp;
         
        if (!txt_usuario.getText().trim().equals("") && !txt_password.getPassword().toString().trim().equals("")){
           
            ctrl_usuario usuario = new ctrl_usuario(
                       0, 
                       "", 
                       txt_usuario.getText().trim(), 
                       password, 
                       "", 
                       0, 
                       "",
                       0,
                       "",
                       0,
                       true,
                       ""
            );
            
         
         
            resp = usuario.verificar_password();
            
            if (resp){
                frm_principalM1 frame = new frm_principalM1();
                frame.setVisible(true);
                     
                
                this.dispose();
               
            }
            else{
                                
                JOptionPane.showMessageDialog(null, "USUARIO O CONTRASEÑA NO VALIDO", "VALIDACION DE USUARIO", JOptionPane.ERROR_MESSAGE,null);
                txt_password.setText("");
                txt_password.requestFocus();
                txt_password.requestFocusInWindow();
            }
        }
        else{
               JOptionPane.showMessageDialog(null, "USUARIO O CONTRASEÑA NO VALIDO", "VALIDACION DE USUARIO", JOptionPane.ERROR_MESSAGE,null);
        }
            
            
    }//GEN-LAST:event_bot_ingresarActionPerformed

