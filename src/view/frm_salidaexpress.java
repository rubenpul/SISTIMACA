    private void bot_contabilizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_contabilizarActionPerformed
        String fecha_salida;
        class_salida modelo_salida;
        
        fecha_salida = funciones.fecha_convertir_date(date_salida, 1);
        
           if (!cmb_almacen.getSelectedItem().equals("Seleccione...") &&
               !cmb_ubicacion.getSelectedItem().equals("Seleccione...") &&    
               !fecha_salida.equals("") &&
               !txt_destino.equals("") &&    
               !txt_numeromaterial.getText().equals("")){
               
               
                ctrl_salida salida  = new ctrl_salida(
                    0, 
                    Integer.parseInt(txt_posicion.getText().trim()), 
                    Integer.parseInt(txt_numeromaterial.getText().trim()),
                    "", //descripcion material
                    Integer.parseInt(frm_principalM1.lbl_cedula.getText()), 
                    frm_principalM1.lbl_nombres.getText(), 
                    Integer.parseInt(cmb_almacen.getSelectedItem().toString().substring(0,3).trim()),
                    cmb_almacen.getSelectedItem().toString().substring(5,cmb_almacen.getSelectedItem().toString().length()).trim(),
                    Integer.parseInt(cmb_ubicacion.getSelectedItem().toString().substring(0,3).trim()),
                    cmb_ubicacion.getSelectedItem().toString().substring(5,cmb_ubicacion.getSelectedItem().toString().length()).trim(),
                    fecha_salida,
                    Integer.parseInt(txt_id.getText().trim()),
                    3,
                    Integer.parseInt(txt_iddocumento.getText().trim()),    
                    Integer.parseInt(spin_cantidad.getValue().toString()),
                    txt_destino.getText().trim(), 
                    "", 
                    true
                           
            ); 
                bot_contabilizar.setEnabled(false);
                modelo_salida = salida.crear_salida_express(table_posicionsalida);
                
                if (modelo_salida.isStatus()){
                    txt_iddocumento.setText(modelo_salida.getId_documento().toString());
                    txt_posicion.setText(modelo_salida.getPosicion().toString());
                    
                    bot_contabilizar.setEnabled(false);
                    bot_verdocumento.setEnabled(true);
                    
                    txt_numeromaterial.setText("");
                    txt_descripcionmaterial.setText("");
                    spin_cantidad.setValue(1);
                    cmb_almacen.removeAllItems();
                    cmb_ubicacion.removeAllItems();
                    txt_id.setText("0");
                    txt_idproyecto.setText("");
                    txt_descripcionproyecto.setText("");      
                    date_salida.setDate(null);
                    txt_destino.setText("");
                    lbl_foto.setIcon(null);   
                }
                
           }
           else{
               JOptionPane.showMessageDialog(null,"DATOS INCOMPLETOS");
           }
    }//GEN-LAST:event_bot_contabilizarActionPerformed

