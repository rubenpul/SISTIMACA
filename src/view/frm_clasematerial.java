    private void bot_crearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_crearActionPerformed
       class_clasematerial modelo_clasematerial;
      ctrl_comun control;
        
        if(!txt_idclasematerial.getText().equals("") && !txt_clasematerial.getText().equals("") ) {
                
                ctrl_clasematerial clasematerial = new ctrl_clasematerial(0,txt_idclasematerial.getText(),txt_clasematerial.getText(),0);
                               
                modelo_clasematerial = clasematerial.actualizar_clasematerial();
                
                if (modelo_clasematerial.isStatus()){
                    
                   clasematerial.consulta_clasematerial(table_clasematerial);
                         
                   control = new ctrl_comun(3); 
                   cmb_clasematerial =  control.cargar_combo(cmb_clasematerial,txt_clasematerial.getText().toUpperCase().trim());
                   
                }
                
        }
        else{
            JOptionPane.showMessageDialog(null,"DATOS INCOMPLETOS PARA GUARDAR");
        } 
    }//GEN-LAST:event_bot_crearActionPerformed

