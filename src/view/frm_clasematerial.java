    private void bot_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bot_actualizarActionPerformed
        class_clasematerial modelo_clasematerial= null; 
        ctrl_comun control;
        boolean resp = true;
       
        
        if(!txt_nuevaclasematerial.getText().equals("") && !txt_nuevoidclasematerial.getText().equals("") && !cmb_clasematerial.getSelectedItem().equals("Seleccione...")  ) {

           
            
            if (!txt_nuevoidclasematerial.getText().equals(cmb_clasematerial.getSelectedItem().toString().substring(0,3).trim())){
                
                ctrl_clasematerial clasematerial = new ctrl_clasematerial(0,txt_nuevoidclasematerial.getText(),txt_nuevaclasematerial.getText(),Integer.parseInt(cmb_clasematerial.getSelectedItem().toString().substring(0,3).trim()));
                
                modelo_clasematerial = clasematerial.actualizar_clasematerial_detalle();

                if (modelo_clasematerial.isStatus()){
                    
                    clasematerial.consulta_clasematerial(table_clasematerial);
                                                    
                    control = new ctrl_comun(3); 
                    cmb_clasematerial =  control.cargar_combo(cmb_clasematerial,txt_clasematerial.getText().toUpperCase().trim());
                    
                    txt_nuevaclasematerial.setText("");
                    
                    txt_nuevoidclasematerial.setText("");
                                   
                }
            }
        }
        else{

            JOptionPane.showMessageDialog(null,"DATOS INCOMPLETOS PARA ACTUALIZAR");
        }
    }//GEN-LAST:event_bot_actualizarActionPerformed

